    private void CliqueBtnEntrar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CliqueBtnEntrar
        String usuario = this.getLogin().getText();
        String senha = this.getSenha().getText();
        
        try {
            LoginController controller = new LoginController(usuario, senha);
            if(controller.entrar()){
               this.dispose();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_CliqueBtnEntrar


